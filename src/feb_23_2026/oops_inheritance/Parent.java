package feb_23_2026.oops_inheritance;

public class Parent {

    // Static variable
    public static char staticValue = 'P';

    // Static method
    public static void displayStaticMethod() {
        System.out.println("Static method from Parent class");
    }

    // Instance variable
    public String instanceValue = "Parent Class";

    // Instance method
    public void displayInstanceMethod() {
        System.out.println("Instance method from Parent class");
    }
}